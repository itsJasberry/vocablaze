import db from "@/lib/db";
import { cache } from "react";

export const getUserByEmail = cache(async (email: string) => {
  try {
    const user = await db.user.findUnique({
      where: {
        email,
      },
    });
    return user;
  } catch (error) {
    console.log(error);
    return null;
  }
});

